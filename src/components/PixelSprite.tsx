export const createPixelSpriteCanvas = (seed: string, name: string = "", size: number = 16): HTMLCanvasElement => {
    const canvas = document.createElement('canvas');
    const pixelScale = 8;
    const border = 1;
    canvas.width = (size + border * 2) * pixelScale;
    canvas.height = (size + border * 2) * pixelScale;
    
    const ctx = canvas.getContext('2d');
    if (!ctx) return canvas;

    ctx.imageSmoothingEnabled = false;

    const n = (name || seed).toUpperCase();
    let spriteKey = 'HUMANOID';

    // 敵キャラのマッチング
    if (n.includes('SLIME')) spriteKey = 'SLIME';
    else if (n.includes('GHOST') || n.includes('幽霊')) spriteKey = 'GHOST';
    else if (n.includes('BAT') || n.includes('コウモリ')) spriteKey = 'BAT';
    else if (n.includes('DRAIN') || n.includes('死体')) spriteKey = 'DRAIN';
    else if (n.includes('DRAGON') || n.includes('ドラゴン')) spriteKey = 'DRAGON';
    else if (n.includes('METAL') || n.includes('メタル')) spriteKey = 'METAL';
    else if (n.includes('MANDRAKE') || n.includes('植物')) spriteKey = 'MANDRAKE';
    else if (n.includes('GOLEM') || n.includes('模型') || n.includes('石像')) spriteKey = 'GOLEM';
    else if (n.includes('NINJA') || n.includes('忍者')) spriteKey = 'NINJA';
    else if (n.includes('BOSS') || n.includes('校長')) spriteKey = 'GOLEM';
    else if (n.includes('先生') || n.includes('教頭')) spriteKey = 'TEACHER';
    else if (n.includes('鳥') || n.includes('カラス')) spriteKey = 'FLIER';
    else if (n.includes('犬') || n.includes('獣')) spriteKey = 'BEAST';

    // アイテムのマッチング
    else if (n.includes('剣') || n.includes('SWORD') || n.includes('バット') || n.includes('武器') || n.includes('ペン')) spriteKey = 'SWORD';
    else if (n.includes('盾') || n.includes('SHIELD') || n.includes('ランドセル') || n.includes('防災') || n.includes('エプロン')) spriteKey = 'SHIELD';
    else if (n.includes('ノート') || n.includes('本') || n.includes('勉強') || n.includes('紙')) spriteKey = 'NOTEBOOK';
    else if (n.includes('薬') || n.includes('ポーション') || n.includes('肉') || n.includes('飯')) spriteKey = 'POTION';
    else if (n.includes('傘') || n.includes('STAFF')) spriteKey = 'UMBRELLA';

    const template = SPRITE_TEMPLATES[spriteKey] || SPRITE_TEMPLATES.HUMANOID;

    // パレット生成
    let hash = 0;
    for (let i = 0; i < n.length; i++) {
      hash = n.charCodeAt(i) + ((hash << 5) - hash);
    }
    
    const palettes = [
      ['#2E7D32', '#66BB6A'], // Green
      ['#C62828', '#EF5350'], // Red
      ['#1565C0', '#42A5F5'], // Blue
      ['#424242', '#BDBDBD'], // Grey
      ['#6A1B9A', '#AB47BC'], // Purple
      ['#5D4037', '#8D6E63'], // Brown
      ['#00ACC1', '#26C6DA'], // Cyan
      ['#F57F17', '#FBC02D'], // Yellow/Gold
    ];

    let palette = palettes[Math.abs(hash) % palettes.length];
    
    // キーワードによる色固定
    if (n.includes('赤') || n.includes('火') || n.includes('赤')) palette = palettes[1];
    else if (n.includes('青') || n.includes('水') || n.includes('氷')) palette = palettes[2];
    else if (n.includes('緑') || n.includes('草')) palette = palettes[0];
    else if (n.includes('金') || n.includes('黄') || n.includes('METAL')) palette = palettes[7];
    else if (n.includes('紫') || n.includes('毒') || n.includes('闇')) palette = palettes[4];
    else if (n.includes('黒')) palette = ['#212121', '#424242'];
    else if (n.includes('白')) palette = ['#EEEEEE', '#FFFFFF'];

    const mainColor = palette[0];
    const highlightColor = palette[1];
    const outlineColor = '#000000';

    for (let y = 0; y < 16; y++) {
      for (let x = 0; x < 16; x++) {
        const char = template[y][x];
        if (char === '.') continue;
        ctx.fillStyle = char === '%' ? highlightColor : (char === '@' ? outlineColor : mainColor);
        ctx.fillRect((x + border) * pixelScale, (y + border) * pixelScale, pixelScale, pixelScale);
      }
    }
    return canvas;
};

const PixelSprite: React.FC<PixelSpriteProps> = ({ seed, name = "", className, size = 16 }) => {
  const canvasRef = useRef<HTMLCanvasElement>(null);

  useEffect(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const generated = createPixelSpriteCanvas(seed, name, size);
    canvas.width = generated.width;
    canvas.height = generated.height;
    const ctx = canvas.getContext('2d');
    if (ctx) ctx.drawImage(generated, 0, 0);
  }, [seed, name, size]);

  return <canvas ref={canvasRef} className={className} style={{ imageRendering: 'pixelated' }} />;
};

export default PixelSprite;
