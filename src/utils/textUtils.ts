export const trans = (text: string, languageMode: LanguageMode | string): string => {
  if (languageMode === 'JAPANESE') return text;
  if (!text) return text;

  // 1. Check exact match
  if (DICTIONARY[text]) return DICTIONARY[text];

  // 2. Check partial replacement
  let result = text;
  // Sort keys by length (descending) to match longest phrases first
  const keys = Object.keys(DICTIONARY).sort((a, b) => b.length - a.length);
  
  keys.forEach(key => {
     if (key.length >= 2 && result.includes(key)) { 
        result = result.split(key).join(DICTIONARY[key]);
     }
  });
  
  return result;
};
